    private void btcalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btcalcularActionPerformed
   
   float [] T = new float [365]; 
    float menor = 100, maior = 0, media, soma = 0;
    int dias_inferiores = 0;
String mensagemTexto = " ";

        for (int i = 0; i < 365; i++) {
            String input = JOptionPane.showInputDialog( this,"Digite a temperatura do dia "+(i+1)+" : ");
T [i] = Integer.parseInt(input);
        soma += T[i];

        
        if (T[i] > maior) {
            maior = T[i];
        }
        if (T[i] < menor) {
            menor = T[i];
        }
    }  
      media = soma / 365;
    for (int i = 0; i < 365; i++) {
        if (T[i] < media) {
            dias_inferiores++;
        }
    }
    mensagemTexto +="A soma das temperaturas e : "+soma+"\n"+"A media anual  : "+media+"\n"+"A maior temperatura e : "+maior+
            "\n"+"A menor temperatura e : "+menor+"\n"+
            "A quantidade de dias com temperatura inferior a media e : "+dias_inferiores ;
mensagem.setText(mensagemTexto);
return;


    }//GEN-LAST:event_btcalcularActionPerformed

